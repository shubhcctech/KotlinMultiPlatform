class Operations {




        fun add(num1:Int,num2:Int): Int {
            val sum:Int = num1+num2
            return sum
        }

        fun multiply(num1:Int,num2:Int):Int{
            val sum = num1*num2
            return sum
        }
        fun divide(num1:Int,num2:Int):Int{
            val sum = num1/num2
            return sum
        }
        fun subtract(num1:Int,num2:Int):Int{
            val sum = num1-num2
            return sum
        }

    }

